# informar 2 números
# calcular o quadrado de cada um deles e imprimir

print('CALCULADORA DE QUADRADOS')
num1 = int(input('Informe o primeiro número: '))
num2 = int(input('Informe o segundo número: '))

quadrado1 = num1 ** 2
quadrado2 = num2 ** 2

print(f'{num1} ao quadrado é {quadrado1}')
print(f'{num2} ao quadrado é {quadrado2}')
